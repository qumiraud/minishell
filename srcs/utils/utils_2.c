#include "../../includes/minishell.h"

void	skip_whitespace(char **src)
{
	while (**src == ' ' || (**src >= 9 && **src <= 13))
		(*src)++;
}

int	is_operator(char c)
{
	return (c == '|' || c == '>' || c == '<');
}
